class Cutout(object):
    def __init__(self, length):
        self.length = length

    def __call__(self, img):
        h, w = img.size(1), img.size(2)
        mask = np.ones((h, w), np.float32)
        y = np.random.randint(h)
        x = np.random.randint(w)

        y1 = np.clip(y - self.length // 2, 0, h)
        y2 = np.clip(y + self.length // 2, 0, h)
        x1 = np.clip(x - self.length // 2, 0, w)
        x2 = np.clip(x + self.length // 2, 0, w)

        mask[y1: y2, x1: x2] = 0.
        mask = torch.from_numpy(mask)
        mask = mask.expand_as(img)
        img *= mask
        return img

def get_incumbent_list(y):
    incumbent = y[0]
    y_inc = [incumbent]
    iss = [0]
    c = 0
    for i in range(1, len(y)):
        iss.append(c)
        if y[i] <= incumbent:
            c = i
            incumbent = y[i]
        y_inc.append(incumbent)
    return y_inc, iss

def plot(res, title, incumbent=False):
    if incumbent:
        res = get_incumbent_list(res)
    plt.step(range(20),res)
    plt.grid()
    plt.ylabel('y')
    plt.xlabel('Iteration')
    plt.title(title)
    plt.show()


